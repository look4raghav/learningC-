#include <stdio.h>
#include <math.h>
int main()
{
	int a = 3;
	int b = 5;
	printf("The value of a + b is : %d\n", a+b);
	printf("The value of a - b is : %d\n", a-b);
	printf("The value of a / b is : %f\n", a/b);
	printf("The value of a * b is : %d\n", a*b);

	printf("5 when divided by 2 leaves a remainder of %d\n, 5%2");
	return 0;
	printf("The value of 4^5 is : %f\n", pow(4, 5));
}